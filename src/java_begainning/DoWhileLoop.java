
package java_begainning;


public class DoWhileLoop {
    
    public static void main(String[] args) {
        System.out.println("This 10 time is \"Whilw Loop\"");
        int i=1;
        while(i<=10){
            
            System.out.println(i+" Bangladesh");
            i++;
        }
        System.out.println("This 10 time is \"do Whilw Loop\"");
        int j=1;
        do{
            
            System.out.println(j+" Bangladesh");
            j++;
            
            
        }while(j<=10);
        
        System.out.println("This 10 time is \"For Loop\"");
        for(int k=1;k<=10;k++){
            System.out.println(k+" Bangladesh");
        }
    }
}
